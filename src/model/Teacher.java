package model;

public class Teacher {
	public String card, name, email, phone, estate, password, role;
	
	public Teacher(String card, String name, String email, String phone, String estate, String password, String role) {
		super();
		this.card = card;
		this.name = name;
		this.email = email;
		this.phone = phone;
		this.estate = estate;
		this.password = password;
		this.role = role;
	}
}
